class Bitwise2{
      public static void main(String[] args) {
         int num = 25, result1, result2;

         result1 = num << 1;
	 result2 = num >> 1;
         System.out.println("The original number is: " + num);
         System.out.println("The number multiplied by two is: " + result1);
         System.out.println("The number divided by two is: " + result2);
      }
   }
